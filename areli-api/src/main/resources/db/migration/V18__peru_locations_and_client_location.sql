CREATE TABLE IF NOT EXISTS peru_departments (
    ubigeo VARCHAR(2) PRIMARY KEY,
    name VARCHAR(120) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS peru_provinces (
    ubigeo VARCHAR(4) PRIMARY KEY,
    department_ubigeo VARCHAR(2) NOT NULL REFERENCES peru_departments(ubigeo),
    name VARCHAR(120) NOT NULL,
    CONSTRAINT uq_peru_provinces_department_name UNIQUE (department_ubigeo, name)
);

CREATE TABLE IF NOT EXISTS peru_districts (
    ubigeo VARCHAR(6) PRIMARY KEY,
    department_ubigeo VARCHAR(2) NOT NULL REFERENCES peru_departments(ubigeo),
    province_ubigeo VARCHAR(4) NOT NULL REFERENCES peru_provinces(ubigeo),
    name VARCHAR(140) NOT NULL,
    CONSTRAINT uq_peru_districts_province_name UNIQUE (province_ubigeo, name)
);

ALTER TABLE clients ADD COLUMN IF NOT EXISTS province VARCHAR(120);
ALTER TABLE clients ADD COLUMN IF NOT EXISTS district VARCHAR(140);
ALTER TABLE clients ADD COLUMN IF NOT EXISTS province_ubigeo VARCHAR(4);
ALTER TABLE clients ADD COLUMN IF NOT EXISTS district_ubigeo VARCHAR(6);

CREATE INDEX IF NOT EXISTS idx_clients_province_ubigeo ON clients(province_ubigeo);
CREATE INDEX IF NOT EXISTS idx_clients_district_ubigeo ON clients(district_ubigeo);
CREATE INDEX IF NOT EXISTS idx_peru_provinces_department ON peru_provinces(department_ubigeo);
CREATE INDEX IF NOT EXISTS idx_peru_districts_province ON peru_districts(province_ubigeo);

